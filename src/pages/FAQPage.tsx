const FAQItem: React.FC<FAQItemProps> = ({ question, answer, isOpen, toggleOpen, category }) => {
  return (
    <div className="bg-black/30 backdrop-blur-sm border border-white/10 rounded-lg overflow-hidden transition-all duration-300 hover:border-white/30 hover:bg-black/40 mb-4">
      <button 
        className="flex items-center justify-between w-full p-4 text-white font-semibold hover:text-gray-300 transition-colors"
        onClick={toggleOpen}
      >
        <span>{question}</span>
        {isOpen ? <ChevronUp className="h-5 w-5" /> : <ChevronDown className="h-5 w-5" />}
      </button>
      <div className={`p-4 text-gray-300 border-t border-white/10 ${isOpen ? 'block' : 'hidden'}`}>
        {answer}
      </div>
      <div className={`px-4 pb-4 text-sm text-gray-400 ${isOpen ? 'block' : 'hidden'}`}>
        Category: {category}
      </div>
    </div>
  );
};

